 <span class="title">watched tomatoes</span> <br><b>Wednesday, July 14 2010</b>  
 
 
<safe>
</div><div style="text-indent:3em">It rained for much of the night and well into today, doing much to bring an end to the recent drought.  But nothing, not rain, not sun, seems to be having any effect on the many large green tomatoes hanging from vines in my two tomato patches.  They remain unchanged in size and shape for weeks and  I guess I'm supposed to have faith at some point that they'll end this charade and turn red. But evidently a watched tomato never does.   At least the cherry tomatoes are playing nice.  They're turning red in a tight schedule, each having matured precisely the same number days before doing so.  For this reason the tomatoes nearest the supporting branch turn red first, and then gradually tomatoes further out follow suit.<br>
</div><div style="text-indent:3em">But we're not the only one dealing with reluctantly-turning tomatoes.  When Gretchen picked up our CSA vegetables today, there were still no tomatoes in the mix. 
<p>
</div><div style="text-indent:3em">Meanwhile our new cat Nigel has been demonstrating that he isn't the complete picture of perfection we'd first thought him to be.  I'd noticed the smallest hint of the smell of cat piss last night, and by today it was something one couldn't ignore. It turned out that Nigel had been pissing on Sally's dog bed and also on   the scattered pile of shoes just inside the front door.  I took Sally's bed apart, washed it thoroughly, and spread it out on the Subaru to dry.  But that was just addressing symptoms;  there didn't seem to be any obvious way to make Nigel stop pissing on things.  We do have a spray that supposedly discourages cats from pissing on objects that have its fragrance, and I can try that.  But I was mostly hoping that the problem was an adjustment issue and that Nigel will stop pissing on random things once he feels completely acclimated.
 <p>
 </div><div style="text-indent:3em">This afternoon I managed to successfully install the new pressure switch on my basement antifreeze supply, and not only did I only waste a few drops of antifreeze in the process, but the switch's cutoff pressure level was easy to reduce and it worked like a charm.
 


